@tool
def calculate_budget(total_budget: int, expenses: str) -> str:
    """
    Tính toán ngân sách còn lại sau khi trừ các khoản chi phí.

    Tham số:
    - total_budget: tổng ngân sách ban đầu (VNĐ)
    - expenses: chuỗi mô tả các khoản chi, format:
      'tên khoản:số_tiền,tên khoản:số_tiền'
      VD: 'vé máy bay:890000,khách sạn:650000'

    Trả về:
    - Bảng chi phí + số tiền còn lại (hoặc cảnh báo vượt ngân sách)
    """

    try:
        # Parse expenses -> dict
        expense_dict = {}
        items = expenses.split(",")

        for item in items:
            if ":" not in item:
                return "❌ Lỗi định dạng: mỗi khoản phải có dạng 'tên:số_tiền'"

            name, value = item.split(":", 1)
            name = name.strip()
            value = value.strip()

            if not value.isdigit():
                return f"❌ Lỗi số tiền không hợp lệ ở khoản '{name}'"

            expense_dict[name] = int(value)

        # Tính toán
        total_expense = sum(expense_dict.values())
        remaining = total_budget - total_expense

        # Format tiền
        def format_vnd(amount):
            return f"{amount:,.0f}₫".replace(",", ".")

        # Build output
        result = "📊 Bảng chi phí:\n"

        for name, value in expense_dict.items():
            result += f"- {name.title()}: {format_vnd(value)}\n"

        result += f"\nTổng chi: {format_vnd(total_expense)}"
        result += f"\nNgân sách: {format_vnd(total_budget)}"

        if remaining >= 0:
            result += f"\nCòn lại: {format_vnd(remaining)}"
        else:
            result += f"\n⚠️ Vượt ngân sách {format_vnd(abs(remaining))}! Cần điều chỉnh."

        return result

    except Exception as e:
        return f"❌ Lỗi xử lý dữ liệu: {str(e)}"